# Set is an unordered collection of items and immutable and does not allow duplicate Values.
# Since it is unordered indexing has no meaning so does slicing operator

s1 = {'python',6,7.6,9.0,8}
print(s1)

s2 = {'Vaibhav',10,15,17}
print(s2)

print(s1 and s2) # intersection of two sets
print(s1 or s2) # Union of 2 sets